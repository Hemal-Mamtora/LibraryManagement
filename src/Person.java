
public class Person {
	String id;
	String name;
	String username;
	String pwd;
	boolean isLibrarian;
	
	Person(String id, String name, String username, String pwd, boolean isLibrarian){
		this.id = id;
		this.name = name;
		this.username = username;
		this.pwd = pwd;
		this.isLibrarian = isLibrarian;
	}
}
